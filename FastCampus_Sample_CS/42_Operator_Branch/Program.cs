using System;

/*------------------------------------------------
 * NAME : 42_Operator_Branch
 * DESC : if(조건문), 조건문이 가지는 값은 true/false 기초
 -------------------------------------------------*/
namespace _Operator_Branch
{
    class MainClass
    {
        public static void Main(string[] args)
        {
            bool isBool = false;

            if(isBool) // 조건문이 가지는 값은(true/false)
            {
                Console.WriteLine("isBool : {0}", isBool);
            }
            else
            {
                Console.WriteLine("isBool : {0}", isBool);
            }
        }
    }
}
